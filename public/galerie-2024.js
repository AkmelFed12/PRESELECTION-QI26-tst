const status = document.getElementById('galleryStatus');
const sliderWrap = document.getElementById('sliderWrap');
const slideImage = document.getElementById('slideImage');
const slideCounter = document.getElementById('slideCounter');
const prevBtn = document.getElementById('prevBtn');
const nextBtn = document.getElementById('nextBtn');
const downloadBtn = document.getElementById('downloadBtn');
const togglePlay = document.getElementById('togglePlay');
const thumbs = document.getElementById('thumbs');
const speedSelect = document.getElementById('speedSelect');
const gridGallery = document.getElementById('gridGallery');

let index = 0;
let timer = null;
let isPlaying = true;
let intervalMs = 5000;

function showSlide(i) {
  if (!images.length) return;
  index = (i + images.length) % images.length;
  const src = `galerie-2025/${encodeURIComponent(images[index])}`;
  slideImage.src = src;
  downloadBtn.href = src;
  downloadBtn.setAttribute('download', images[index]);
  slideCounter.textContent = `${index + 1} / ${images.length}`;
  if (thumbs) {
    const active = thumbs.querySelector('.thumb.active');
    if (active) active.classList.remove('active');
    const current = thumbs.querySelector(`[data-idx="${index}"]`);
    if (current) current.classList.add('active');
  }
}

if (!images.length) {
  status.textContent = 'Aucune photo disponible.';
} else {
  status.style.display = 'none';
  sliderWrap.style.display = 'block';
  showSlide(0);
}

function restartTimer() {
  if (timer) clearInterval(timer);
  if (!isPlaying) return;
  timer = setInterval(() => showSlide(index + 1), intervalMs);
}

prevBtn?.addEventListener('click', () => {
  showSlide(index - 1);
  restartTimer();
});
nextBtn?.addEventListener('click', () => {
  showSlide(index + 1);
  restartTimer();
});

if (images.length) {
  restartTimer();
}

togglePlay?.addEventListener('click', () => {
  isPlaying = !isPlaying;
  togglePlay.textContent = isPlaying ? 'Pause' : 'Lecture';
  restartTimer();
});

if (thumbs) {
  thumbs.innerHTML = images
    .map(
      (name, idx) =>
        `<img class="thumb${idx === 0 ? ' active' : ''}" data-idx="${idx}" src="galerie-2025/${encodeURIComponent(name)}" alt="thumb" style="width:70px;height:50px;object-fit:cover;border-radius:6px;border:2px solid transparent;cursor:pointer;" />`,
    )
    .join('');
  thumbs.addEventListener('click', (e) => {
    const img = e.target.closest('img[data-idx]');
    if (!img) return;
    const idx = Number(img.dataset.idx);
    showSlide(idx);
    restartTimer();
  });
}

speedSelect?.addEventListener('change', () => {
  intervalMs = Number(speedSelect.value || 5000);
  restartTimer();
});

if (gridGallery) {
  gridGallery.innerHTML = images
    .map(
      (name, idx) =>
        `<img class="grid-thumb" data-idx="${idx}" src="galerie-2025/${encodeURIComponent(name)}" alt="photo" />`,
    )
    .join('');
  gridGallery.addEventListener('click', (e) => {
    const img = e.target.closest('img[data-idx]');
    if (!img) return;
    const idx = Number(img.dataset.idx);
    showSlide(idx);
    sliderWrap.scrollIntoView({ behavior: 'smooth', block: 'start' });
    restartTimer();
  });
}
